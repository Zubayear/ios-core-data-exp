import UIKit

extension UIButton {
    
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.6394329667, green: 0.1201454774, blue: 0.3139832616, alpha: 1)
    }
    
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.6394329667, green: 0.1201454774, blue: 0.3139832616, alpha: 0.5)
    }
}
